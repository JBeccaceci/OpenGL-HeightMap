#ifndef Texture_h
#define Texture_h

////////////////////////////////////////////////////////////
/// Headers
////////////////////////////////////////////////////////////
#include <stdio.h>
#include <stdlib.h>

////////////////////////////////////////////////////////////
/// HeightMap data
////////////////////////////////////////////////////////////
struct TextureData
{
    unsigned int TextureID;
    int Loaded;
    struct TargaHeader * TGAHeader;
    unsigned char * pTexture;
    int Size;
};

////////////////////////////////////////////////////////////
/// TGA header
////////////////////////////////////////////////////////////
struct TargaHeader
{
    unsigned char  Data[12];
    unsigned short Width;
    unsigned short Height;
    unsigned char  Bpp;
    unsigned char  Data2;
};

////////////////////////////////////////////////////////////
/// Create texture
////////////////////////////////////////////////////////////
struct TextureData * CreateTexture();

////////////////////////////////////////////////////////////
/// Load texture
////////////////////////////////////////////////////////////
int TextureLoad(struct TextureData * Texture, char * Name, int textureID, int Wrap);

////////////////////////////////////////////////////////////
/// Destroy texture
////////////////////////////////////////////////////////////
void TextureDestroy(struct TextureData * Texture);

#endif /* Texture_h */
